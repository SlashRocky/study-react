import Head from "next/head";
import { useRouter } from 'next/router';

import { useFetch } from "src/hooks/useFetch";
import { PostTitleByComment } from "src/components/Post/PostTitleByComment";
import { data } from "autoprefixer";
import { BASE_ENDPOINT } from 'src/utils/const';

export const Comment = () => {
  const router = useRouter();

  const { data, error, isLoading } = useFetch(
    router.query.id ?
    `${BASE_ENDPOINT}/comments/${router.query.id }`
    : null
  );

  if (isLoading) {
    return(
      <div>ローディング中です。</div>
    );
  }

  if (error) {
    return(
      <div>{error.message}</div>
    );
  }

  return (
    <div>
      <Head>
        <title>{data?.name}</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className="text-lg">
        {data?.name} ({data?.email})
      </div>
      <h1 className="text-3xl font-bold">{data?.body}</h1>
      <div className="mt-2">
        <PostTitleByComment postId={data.postId} />
      </div>
    </div>
  );
}
